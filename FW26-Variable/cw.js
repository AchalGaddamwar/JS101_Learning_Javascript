let x = 5;
x += 3;
console.log(x);

x += 5;//x = x + 5;
console.log(x);

x -= 1; //x = x-1
console.log(x);

x -= 3;
console.log(x);

x++; //increasing value by 1, post increment(executes first increment later)
console.log(x);
x--; //decreasing value by 1, post decrement(executes first decrement later)
console.log(x);

--x; // pre decrement
console.log(x);

++x;
console.log(x);//pre increment